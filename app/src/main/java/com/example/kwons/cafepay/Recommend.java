package com.example.kwons.cafepay;

public class Recommend {

    String cafeLocation;
    String cafeMenu;
    String cafeName;
    int evaluation;
    int id;
    int price;
    String tumblerSerial;
    String userId;

    public String getCafeLocation() {
        return cafeLocation;
    }

    public void setCafeLocation(String cafeLocation) {
        this.cafeLocation = cafeLocation;
    }

    public String getCafeMenu() {
        return cafeMenu;
    }

    public void setCafeMenu(String cafeMenu) {
        this.cafeMenu = cafeMenu;
    }

    public String getCafeName() {
        return cafeName;
    }

    public void setCafeName(String cafeName) {
        this.cafeName = cafeName;
    }

    public int getEvaluation() {
        return evaluation;
    }

    public void setEvaluation(int evaluation) {
        this.evaluation = evaluation;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getTumblerSerial() {
        return tumblerSerial;
    }

    public void setTumblerSerial(String tumblerSerial) {
        this.tumblerSerial = tumblerSerial;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

}


